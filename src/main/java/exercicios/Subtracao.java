package exercicios;

public class Subtracao {
    private int numero1;
    private int numero2;

    public void setNumero1(int numero1){
        this.numero1 = numero1;
    }
    public void setNumero2(int numero2){
        this.numero2 = numero2;
    }

    int calcularSubtracao(int num1, int num2){
        return num1 - num2;
    }
}
